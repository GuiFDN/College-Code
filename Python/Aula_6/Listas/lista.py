# uma lista de números inteiros
minha_lista = [1, 2, 3]
print (minha_lista)

# uma lista pode conter itens de qualquer tipo

uma_lista_misturada = [1, 'nome, 0.75']
print (uma_lista_misturada)

# tamanho de uma lista, função len () indica o tamanho da lista, quantidade de dados
print (len (minha_lista))
print (len (uma_lista_misturada))